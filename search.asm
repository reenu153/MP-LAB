MOV SI, 1100h
MOV DI, 1200h
MOV DL, [DI]
MOV BL, 01h
MOV AL, [SI]
REPEAT:
CMP AL, DL
JZ FOUND
INC SI
INC BL
MOV AL, [SI]
CMP AL, 50h
JNZ REPEAT
NOTFOUND:
MOV CX, 00h
MOV [DI+1], CX
MOV [DI+2], CX
MOV [DI+3], CX
JMP FINISH
FOUND:
MOV BH, 0FFh
MOV [DI+1], BH
MOV [DI+2], BL
MOV [DI+3], SI
FINISH:
HLT